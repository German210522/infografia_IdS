/**
 * Renders an SVG icon wrapped in a span with the icon class
 * @param {string} name - Icon name from ICONS map
 * @param {string} extraClass - Additional CSS class(es)
 * @returns {string} HTML string
 */
function icon(name, extraClass = '') {
  const svg = ICONS[name] || '';
  return `<span class="icon ${extraClass}">${svg}</span>`;
}

/**
 * Initialize all icons on page load
 * Replaces elements with data-icon attribute with SVG icons
 */
function initIcons() {
  document.querySelectorAll('[data-icon]').forEach(el => {
    const name = el.getAttribute('data-icon');
    const size = el.getAttribute('data-icon-size') || '';
    if (ICONS[name]) {
      const existingText = el.textContent.trim();
      const iconHtml = `<span class="icon ${size}">${ICONS[name]}</span>`;
      if (existingText) {
        el.innerHTML = iconHtml + ' ' + existingText;
      } else {
        el.innerHTML = iconHtml;
      }
    }
  });
}

// Initialize when DOM is ready
document.addEventListener('DOMContentLoaded', initIcons);
